int bitlen_16(uint16_t x) {
    int ret{0};
    if (x >= 1 << 8) {
        x >>= 8;
        ret = 8;
    }
    ret += static_cast<int>(kLen8Lut[x]);
    return ret;
}

int ctz_16(uint16_t x) {
    if (!x) {
        return 16;
    }
    int ret{kDeBruijn32Lut[static_cast<uint32_t>(x & -x) * kDeBruijn32 >> (32 - 5)]};
    return ret;
}

int popcount_16(uint64_t x) {
    int ret{static_cast<int>(kPop8Lut[x >> 8] + kPop8Lut[x & 0xFF])};
    return ret;
}

